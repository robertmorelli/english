#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
import os
from typing import List, Dict


def format_mb(size_bytes: int) -> str:
    return f"{size_bytes / 1024 / 1024:.2f}"


def read_results(path: str) -> List[Dict[str, str]]:
    with open(path, "r", encoding="utf-8") as f:
        rows = list(csv.DictReader(f, delimiter="\t"))
    return rows


def count_lines(path: str) -> int:
    with open(path, "r", encoding="utf-8") as f:
        return sum(1 for _ in f)


def main() -> int:
    parser = argparse.ArgumentParser(description="Render comparison results as Markdown.")
    parser.add_argument("--results", required=True, help="Path to results.tsv")
    parser.add_argument("--words", required=True, help="Path to normalized words.txt")
    parser.add_argument("--output", required=True, help="Path to output Markdown file")
    args = parser.parse_args()

    results = read_results(args.results)
    raw_bytes = os.path.getsize(args.words)
    keys = count_lines(args.words)

    our_bytes = None
    for row in results:
        if row.get("name") == "our_trie":
            try:
                our_bytes = int(row["bytes"])
            except (KeyError, ValueError):
                our_bytes = None
            break

    lines = []
    lines.append("# Comparison Results")
    lines.append("")
    lines.append(f"Word list: `{args.words}`")
    lines.append(f"Unique keys: {keys:,}")
    lines.append(f"Raw text bytes: {raw_bytes:,}")
    lines.append("")
    lines.append("| name | bytes | MB | ratio to raw | ratio to our_trie |")
    lines.append("| --- | --- | --- | --- | --- |")

    for row in results:
        try:
            name = row["name"]
            size = int(row["bytes"])
        except (KeyError, ValueError):
            continue

        ratio_raw = size / raw_bytes if raw_bytes else 0.0
        ratio_our = size / our_bytes if our_bytes else 0.0

        ratio_raw_s = f"{ratio_raw:.4f}" if raw_bytes else "-"
        ratio_our_s = f"{ratio_our:.4f}" if our_bytes else "-"

        lines.append(f"| {name} | {size} | {format_mb(size)} | {ratio_raw_s} | {ratio_our_s} |")

    lines.append("")
    lines.append("Generated by `comparisons/run_comparisons.sh`.")
    lines.append("")

    os.makedirs(os.path.dirname(args.output), exist_ok=True)
    with open(args.output, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
